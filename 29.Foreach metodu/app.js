let names = ["Alice", "Bob", "John", "Cloud", "Aerith", "Tifa"];

names.forEach(function(name){

    console.log(name);

});

// Or we can use for loop to obtain exact same result

for(let i = 0; i < names.length; i++){

    console.log(names[i]);

}

// But using foreach loop for arrays is more chic :D