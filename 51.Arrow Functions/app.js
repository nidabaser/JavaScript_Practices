// Arrow Functions

const yazdir = () => {
    console.log("Selam")
}
yazdir();

const yazdirParam = (firstName) => {
    console.log("Selam", firstName)
}
yazdirParam("Alice");

const yazdirParam2 = (firstName, lastName) => {
    console.log("Selam", firstName, lastName)
}
yazdirParam2("Alice","Dove");