// Aritmetic Operators

/*
(=) : atama
(+) : arttırma
(-) : azaltma
(*) : çarpma
(/) : bölme
(%) : mod alma / kalanı bulma
(++) : 1 artırma
(--) : 1 azaltma
(**) : üs alma
*/

let point1 = 50;
let point2 = 100;
console.log("Sum of Points: " + point1 + point2); // String
console.log("Sum of Points: " + (point1 + point2)); // Sum of Strings

// "var" yerine "let" kullanımı öneriliyor, bellek tasarrufu için

let a = 10%3;
console.log(a);

let b = 10;
b++;
console.log(b);
b--;
console.log(b);

console.log(2**3);